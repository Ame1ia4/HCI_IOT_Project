"""
ML validator — Phase 2

Loads the trained TFLite model and runs inference on a cropped card image.
Returns (is_valid, confidence) to be combined with the Phase 1 rule-based score.

The model file is generated by training/train_model.py and is not committed
to the repo (it lives in models/card_model.tflite).
"""

import os

import cv2
import numpy as np

MODEL_PATH = os.path.join("models", "card_model.tflite")
IMG_SIZE   = 224  # must match the size used during training


def _load_interpreter():
    """Load the TFLite interpreter. Returns None if the model file is missing."""
    if not os.path.exists(MODEL_PATH):
        return None
    try:
        import tensorflow as tf
        interpreter = tf.lite.Interpreter(model_path=MODEL_PATH)
        interpreter.allocate_tensors()
        return interpreter
    except Exception as e:
        print(f"[ML] Failed to load model: {e}")
        return None


# Load once at import time so the model isn't reloaded on every frame
_interpreter = _load_interpreter()


def _preprocess(card_img):
    """Resize to 224x224, convert BGR→RGB, normalise to [0, 1]."""
    img = cv2.resize(card_img, (IMG_SIZE, IMG_SIZE))
    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    img = img.astype("float32") / 255.0
    return np.expand_dims(img, axis=0)  # shape: (1, 224, 224, 3)


def is_model_available():
    """Return True if the TFLite model has been loaded successfully."""
    return _interpreter is not None


def predict(card_img):
    """
    Run inference on a cropped card image.

    Args:
      card_img: BGR image of the perspective-corrected card.

    Returns:
      (is_valid: bool, confidence: float 0.0–1.0)
      Returns (False, 0.0) if the model file has not been generated yet.
    """
    if _interpreter is None:
        return False, 0.0

    input_details  = _interpreter.get_input_details()
    output_details = _interpreter.get_output_details()

    input_data = _preprocess(card_img)
    _interpreter.set_tensor(input_details[0]["index"], input_data)
    _interpreter.invoke()

    output = _interpreter.get_tensor(output_details[0]["index"])
    confidence = float(output[0][0])  # sigmoid output: 1 = valid, 0 = invalid

    return confidence >= 0.5, round(confidence, 3)
